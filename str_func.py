def str_upper(input_str):
    """
    Принимает на вход строку и возвращает её со всеми заглавными буквами.
    Описание было добавлено изначально дополнительно.
    :param input_str: Строка
    :return:
    """
    return input_str.strip().upper()


def str_title(input_str):
    """
     Делает заглавными первые буквы каждого слова в строке, поступившей на вход функции.
    :param input_str: Строка
    :return:
    """
    return input_str.strip().title()
